class Solution {
        public int minimumSwitchingTimes(int[][] source, int[][] target) {
            int[] map = new int[10001];
            int n = source.length;
            int m = source[0].length;
            int ans = 0;
            for(int i=0;i < n * m;i++){
                map[source[i / m][i % m]]++;
                map[target[i / m][i % m]]--;
            }
            for(int i =0;i < 10001;i++){
                if(map[i] > 0){
                    ans += map[i];
                }
            }
            return ans;
        }
}
